    private void btnParquearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnParquearActionPerformed
        try {
            String placa = txtPlaca.getText();
            String propietario = txtPropietario.getText();
            String tipo = (String) cbTipo.getSelectedItem();
            
            if (placa.isEmpty() || propietario.isEmpty() || tipo == null) {
                JOptionPane.showMessageDialog(null, "Complete todos los campos.");
                return;
            }

            // Crear el vehículo basado en el tipo seleccionado
            Vehiculo vehiculo;
            switch (tipo) {
                case "Carro":
                    vehiculo = new Carro(placa, propietario);
                    break;
                case "Moto":
                    vehiculo = new Moto(placa, propietario);
                    break;
                case "Camión":
                    vehiculo = new Camion(placa, propietario);
                    break;
                default:
                    throw new IllegalArgumentException("Tipo de vehículo no válido.");
            }

            parqueadero.parquearVehiculo(vehiculo);
            Consultas conVehiculo = new Consultas();
            conVehiculo.registrar(vehiculo);
            JOptionPane.showMessageDialog(null, "Vehículo parqueado correctamente.");
            actualizarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnParquearActionPerformed

    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        String placa = JOptionPane.showInputDialog("Ingrese la placa del vehículo a retirar:");
        if (placa != null && !placa.isEmpty()) {
            try {
                parqueadero.retirarVehiculo(placa);
                JOptionPane.showMessageDialog(null, "Vehículo retirado correctamente.");
                actualizarTabla();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnRetirarActionPerformed

