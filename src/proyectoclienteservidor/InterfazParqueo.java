    private void actualizarTabla() {
        // Limpiar la tabla
        modeloTabla.setRowCount(0);

        // Agregar los datos del estado del parqueadero
        for (int i = 0; i < parqueadero.getEspacios().size(); i++) {
            EspacioParqueo espacio = parqueadero.getEspacios().get(i);
            if (espacio.estaOcupado()) {
                Vehiculo vehiculo = espacio.getVehiculo();
                modeloTabla.addRow(new Object[]{
                    (i + 1), "Ocupado", vehiculo.getPlaca(), vehiculo.getPropietaio()
                });
            } else {
                modeloTabla.addRow(new Object[]{(i + 1), "Libre", "-", "-"});
            }
        }
    }

